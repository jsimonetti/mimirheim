"""Unit tests for reporter.inventory.

Covers update(), remove(), and rebuild_from_disk() in isolation using
temporary directories. No MQTT or daemon infrastructure is needed.
"""
from __future__ import annotations

import json
from pathlib import Path

import pytest

from reporter import inventory as inv


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

_TS1 = "2026-04-02T14:00:00Z"
_TS2 = "2026-04-02T14:15:00Z"
_TS3 = "2026-04-02T14:30:00Z"


def _minimal_out(strategy: str = "minimize_cost") -> dict:
    return {
        "strategy": strategy,
        "solve_status": "optimal",
        "naive_cost_eur": 1.0,
        "optimised_cost_eur": 0.8,
        "soc_credit_eur": 0.05,
        "dispatch_suppressed": False,
        "schedule": [
            {"grid_import_kw": 2.0, "grid_export_kw": 0.0, "devices": {}},
            {"grid_import_kw": 1.5, "grid_export_kw": 0.0, "devices": {}},
        ],
    }


def _minimal_inp() -> dict:
    return {"prices": []}


# ---------------------------------------------------------------------------
# update()
# ---------------------------------------------------------------------------

def test_update_creates_inventory_js(tmp_path: Path) -> None:
    """update() must create inventory.js if it does not yet exist."""
    inv.update(tmp_path, _TS1, "2026-04-02T14-00-00Z_report.html", _minimal_inp(), _minimal_out())
    assert (tmp_path / "inventory.js").exists()


def test_update_inventory_js_starts_with_preamble(tmp_path: Path) -> None:
    """inventory.js must start with the JavaScript wrapper comment."""
    inv.update(tmp_path, _TS1, "2026-04-02T14-00-00Z_report.html", _minimal_inp(), _minimal_out())
    content = (tmp_path / "inventory.js").read_text()
    assert content.startswith("// Auto-generated by mimirheim-reporter")


def test_update_appends_entry(tmp_path: Path) -> None:
    """Adding two different timestamps must produce two entries."""
    inv.update(tmp_path, _TS1, "ts1_report.html", _minimal_inp(), _minimal_out())
    inv.update(tmp_path, _TS2, "ts2_report.html", _minimal_inp(), _minimal_out())
    entries = inv._read_inventory(tmp_path)
    assert len(entries) == 2


def test_update_sorted_newest_first(tmp_path: Path) -> None:
    """Entries must be ordered newest-first (descending by ts)."""
    inv.update(tmp_path, _TS1, "ts1_report.html", _minimal_inp(), _minimal_out())
    inv.update(tmp_path, _TS3, "ts3_report.html", _minimal_inp(), _minimal_out())
    inv.update(tmp_path, _TS2, "ts2_report.html", _minimal_inp(), _minimal_out())
    entries = inv._read_inventory(tmp_path)
    tss = [e["ts"] for e in entries]
    assert tss == sorted(tss, reverse=True)


def test_update_replaces_duplicate_ts(tmp_path: Path) -> None:
    """Calling update() twice with the same ts must not create duplicate entries."""
    inv.update(tmp_path, _TS1, "ts1_report.html", _minimal_inp(), _minimal_out("minimize_cost"))
    inv.update(tmp_path, _TS1, "ts1_report_v2.html", _minimal_inp(), _minimal_out("zero_export"))
    entries = inv._read_inventory(tmp_path)
    assert len(entries) == 1
    assert entries[0]["strategy"] == "zero_export"


def test_update_entry_has_expected_keys(tmp_path: Path) -> None:
    """A freshly written entry must contain all expected inventory keys."""
    inv.update(tmp_path, _TS1, "ts1_report.html", _minimal_inp(), _minimal_out())
    entries = inv._read_inventory(tmp_path)
    entry = entries[0]
    required_keys = {
        "ts", "file", "strategy", "solve_status", "horizon_steps",
        "naive_cost_eur", "optimised_cost_eur", "soc_credit_eur",
        "effective_cost_eur", "saving_eur", "dispatch_suppressed",
        "total_import_kwh", "total_export_kwh", "self_sufficiency_pct",
    }
    assert required_keys <= set(entry.keys())


# ---------------------------------------------------------------------------
# remove()
# ---------------------------------------------------------------------------

def test_remove_deletes_entry(tmp_path: Path) -> None:
    """remove() must delete the matching entry and leave others intact."""
    inv.update(tmp_path, _TS1, "ts1_report.html", _minimal_inp(), _minimal_out())
    inv.update(tmp_path, _TS2, "ts2_report.html", _minimal_inp(), _minimal_out())
    inv.remove(tmp_path, _TS1)
    entries = inv._read_inventory(tmp_path)
    assert len(entries) == 1
    assert entries[0]["ts"] == _TS2


def test_remove_noop_on_missing_ts(tmp_path: Path) -> None:
    """remove() must be a no-op when the ts is not present."""
    inv.update(tmp_path, _TS1, "ts1_report.html", _minimal_inp(), _minimal_out())
    inv.remove(tmp_path, "2999-01-01T00:00:00Z")  # does not exist
    entries = inv._read_inventory(tmp_path)
    assert len(entries) == 1



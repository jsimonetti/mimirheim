def test_rebuild_from_disk_empty_dir(tmp_path: Path) -> None:
    """rebuild_from_disk() on an empty output_dir must produce an empty inventory."""
    inv.rebuild_from_disk(tmp_path, tmp_path)
    entries = inv._read_inventory(tmp_path)
    assert entries == []


def test_rebuild_from_disk_picks_up_report(tmp_path: Path) -> None:
    """rebuild_from_disk() must create an entry for every dump pair with a report file."""
    # The safe_ts format as used in filenames: hyphens replace colons in the time part.
    safe_ts = "2026-04-02T14-00-00Z"
    ts = "2026-04-02T14:00:00Z"
    report_file = f"{safe_ts}_report.html"
    # Write dummy report HTML
    (tmp_path / report_file).write_text("<html></html>")
    # Write dump pair
    dump_dir = tmp_path / "dumps"
    dump_dir.mkdir()
    (dump_dir / f"{safe_ts}_input.json").write_text(json.dumps(_minimal_inp()))
    (dump_dir / f"{safe_ts}_output.json").write_text(json.dumps(_minimal_out()))

    inv.rebuild_from_disk(tmp_path, dump_dir)
    entries = inv._read_inventory(tmp_path)
    assert len(entries) == 1
    assert entries[0]["ts"] == ts
    assert entries[0]["file"] == report_file


def test_rebuild_from_disk_stub_entry_for_missing_dump(tmp_path: Path) -> None:
    """rebuild_from_disk() must add a stub entry for a report with no prior inventory
    entry and no dump pair.  The stub ensures GC can count and eventually
    remove the file; without it, orphaned reports accumulate forever.
    """
    safe_ts = "2026-04-02T14-00-00Z"
    ts = "2026-04-02T14:00:00Z"
    report_file = f"{safe_ts}_report.html"
    (tmp_path / report_file).write_text("<html></html>")
    dump_dir = tmp_path / "dumps"
    dump_dir.mkdir()
    # No prior inventory.js and no dump pair files.
    inv.rebuild_from_disk(tmp_path, dump_dir)
    entries = inv._read_inventory(tmp_path)
    assert len(entries) == 1
    assert entries[0]["ts"] == ts
    assert entries[0]["file"] == report_file
    # Stub entry must not contain metric keys (no dump data to compute them from).
    assert "naive_cost_eur" not in entries[0]


def test_rebuild_from_disk_preserves_existing_entry_when_dump_gone(tmp_path: Path) -> None:
    """rebuild_from_disk() must keep an existing full inventory entry even when
    the corresponding dump pair has been deleted.

    This is the core restart resilience guarantee: metrics must not be lost
    just because mimirheim rotated away the source dump files.
    """
    safe_ts = "2026-04-02T14-00-00Z"
    ts = "2026-04-02T14:00:00Z"
    report_file = f"{safe_ts}_report.html"
    dump_dir = tmp_path / "dumps"
    dump_dir.mkdir()

    # First pass: report + dump pair both present → full entry written.
    (tmp_path / report_file).write_text("<html></html>")
    (dump_dir / f"{safe_ts}_input.json").write_text(json.dumps(_minimal_inp()))
    (dump_dir / f"{safe_ts}_output.json").write_text(json.dumps(_minimal_out()))
    inv.rebuild_from_disk(tmp_path, dump_dir)

    # Delete the dump pair, simulating mimirheim dump rotation.
    (dump_dir / f"{safe_ts}_input.json").unlink()
    (dump_dir / f"{safe_ts}_output.json").unlink()

    # Second pass (restart): entry must be preserved from inventory.js.
    inv.rebuild_from_disk(tmp_path, dump_dir)
    entries = inv._read_inventory(tmp_path)
    assert len(entries) == 1
    assert entries[0]["ts"] == ts
    # Full metrics must still be present.
    assert "naive_cost_eur" in entries[0]


def test_rebuild_from_disk_removes_entry_for_deleted_html(tmp_path: Path) -> None:
    """rebuild_from_disk() must remove entries whose HTML file no longer exists."""
    safe_ts = "2026-04-02T14-00-00Z"
    ts = "2026-04-02T14:00:00Z"
    report_file = f"{safe_ts}_report.html"
    dump_dir = tmp_path / "dumps"
    dump_dir.mkdir()

    # Create a full entry.
    (tmp_path / report_file).write_text("<html></html>")
    (dump_dir / f"{safe_ts}_input.json").write_text(json.dumps(_minimal_inp()))
    (dump_dir / f"{safe_ts}_output.json").write_text(json.dumps(_minimal_out()))
    inv.rebuild_from_disk(tmp_path, dump_dir)
    assert len(inv._read_inventory(tmp_path)) == 1

    # GC deletes the HTML; reconcile should remove the entry.
    (tmp_path / report_file).unlink()
    inv.rebuild_from_disk(tmp_path, dump_dir)
    assert inv._read_inventory(tmp_path) == []


def test_rebuild_from_disk_stub_is_gc_visible(tmp_path: Path) -> None:
    """A stub entry must be counted by gc.collect() so the orphaned file is removed."""
    from reporter import gc as reporter_gc

    safe_ts = "2026-04-02T14-00-00Z"
    report_file = f"{safe_ts}_report.html"
    html_path = tmp_path / report_file
    html_path.write_text("<html></html>")
    dump_dir = tmp_path / "dumps"
    dump_dir.mkdir()

    inv.rebuild_from_disk(tmp_path, dump_dir)
    # One report on disk, max_reports=0 means collect nothing.
    reporter_gc.collect(tmp_path, max_reports=0)
    assert html_path.exists()
    # max_reports=1 is already satisfied; still no deletion.
    reporter_gc.collect(tmp_path, max_reports=1)
    assert html_path.exists()

    # Add a second (newer) stub so the first is the oldest.
    safe_ts2 = "2026-04-03T14-00-00Z"
    report_file2 = f"{safe_ts2}_report.html"
    html_path2 = tmp_path / report_file2
    html_path2.write_text("<html></html>")
    inv.rebuild_from_disk(tmp_path, dump_dir)

    # max_reports=1: oldest (safe_ts) must be deleted.
    reporter_gc.collect(tmp_path, max_reports=1)
    assert not html_path.exists()
    assert html_path2.exists()
